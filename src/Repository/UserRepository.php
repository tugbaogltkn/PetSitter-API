    public function findUser($id, $hydrate = Query::HYDRATE_ARRAY)
    {
        return $this->createQueryBuilder('u')
            ->where('u.id = :id')
            ->setParameter('id', $id)
            ->getQuery()
            ->getOneOrNullResult($hydrate);
    }

    public function findUserByUsername($username)
    {
        return $this->createQueryBuilder('u')
            ->where('u.username = :username')
            ->setParameters([
                'username' => $username
            ])
            ->getQuery()
            ->getOneOrNullResult();
    }
